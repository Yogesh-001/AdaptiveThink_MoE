"""
Alpaca Dataset Preparation for General Expert Training
=======================================================

This script downloads an Alpaca-style instruction-following dataset and
converts it into the format needed for training a "General Expert".

What is the General Expert?
---------------------------
While Code and Math experts handle specialized tasks, the General Expert
handles everything else:
- Explanations ("Explain quantum computing")
- Summarization ("Summarize this article")
- Creative writing ("Write a poem about...")
- Q&A ("What is the capital of France?")
- Advice ("How do I improve my resume?")

Why Alpaca?
-----------
- Stanford Alpaca (2023) introduced instruction-following fine-tuning
  using 52K instruction-response pairs generated by GPT
- It covers diverse tasks: open QA, brainstorming, rewriting, classification, etc.
- It's the standard dataset for teaching models to follow instructions
- Paper: "Alpaca: A Strong, Replicable Instruction-Following Model" (Taori et al., 2023)

Dataset structure:
- instruction: The task description
- input: Optional additional context (can be empty)
- output: The expected response

We use "yahma/alpaca-cleaned" — a cleaned version that removes:
- Duplicates
- Low-quality samples
- Hallucinated content
"""

import json
import os
from datasets import load_dataset


def format_sample_to_instruction(sample: dict) -> dict:
    """
    Convert an Alpaca sample into our unified instruction-response format.

    Alpaca has an optional "input" field that provides context.
    When present, we merge it into the instruction for a complete prompt.

    Examples:
    - Without input: instruction="Tell me about gravity" → just use instruction
    - With input: instruction="Summarize the following", input="<article>" 
      → combine them into one instruction

    Parameters
    ----------
    sample : dict
        Alpaca sample with keys: instruction, input, output

    Returns
    -------
    dict
        Formatted with: instruction, response
    """

    # Build instruction: combine instruction + input if input exists
    if sample.get("input") and sample["input"].strip():
        # When there's additional context, include it
        instruction = (
            f"{sample['instruction']}\n\n"
            f"Input: {sample['input']}"
        )
    else:
        # Just the instruction itself
        instruction = sample["instruction"]

    response = sample["output"]

    return {
        "instruction": instruction,
        "response": response,
    }


def prepare_dataset(subset_size: int = 500, output_dir: str = None):
    """
    Download Alpaca-cleaned, format it, and save subsets.

    The full dataset has ~52K samples. We:
    1. Filter out very short responses (< 20 chars) — too low quality
    2. Filter out very long responses (> 2000 chars) — won't fit in context
    3. Sample a diverse subset for initial training

    Why filter?
    -----------
    - Very short responses ("Yes", "No") don't teach the model much
    - Very long responses get truncated during training (wasted compute)
    - Quality > quantity for LoRA fine-tuning on small datasets

    Parameters
    ----------
    subset_size : int
        Number of samples for initial experiment
    output_dir : str
        Where to save formatted data
    """

    if output_dir is None:
        output_dir = os.path.dirname(os.path.abspath(__file__))

    print("Loading Alpaca-cleaned dataset from HuggingFace...")
    # "yahma/alpaca-cleaned" is a community-cleaned version of Stanford Alpaca
    ds = load_dataset("yahma/alpaca-cleaned")

    print(f"\nDataset: {ds}")
    print(f"Sample keys: {list(ds['train'][0].keys())}")
    print(f"\nExample raw sample:")
    print(f"  instruction: {ds['train'][0]['instruction'][:150]}...")
    print(f"  input: {ds['train'][0]['input'][:100] if ds['train'][0]['input'] else '(empty)'}")
    print(f"  output: {ds['train'][0]['output'][:150]}...")

    # Format all samples
    all_samples = []
    skipped_short = 0
    skipped_long = 0

    for sample in ds["train"]:
        # Filter: skip very short responses (not useful for learning)
        if len(sample["output"]) < 20:
            skipped_short += 1
            continue

        # Filter: skip very long responses (would be truncated)
        if len(sample["output"]) > 2000:
            skipped_long += 1
            continue

        formatted = format_sample_to_instruction(sample)
        all_samples.append(formatted)

    print(f"\nFiltering results:")
    print(f"  Skipped (too short <20 chars): {skipped_short}")
    print(f"  Skipped (too long >2000 chars): {skipped_long}")
    print(f"  Kept: {len(all_samples)}")

    # Create subset (take evenly spaced samples for diversity)
    # Instead of just first N, we sample every Kth element
    # This gives us a more diverse subset across topics
    step = max(1, len(all_samples) // subset_size)
    subset = [all_samples[i] for i in range(0, len(all_samples), step)][:subset_size]

    # Save full dataset
    full_path = os.path.join(output_dir, "alpaca_formatted_full.json")
    with open(full_path, "w") as f:
        json.dump(all_samples, f, indent=2)
    print(f"\nSaved full dataset: {full_path} ({len(all_samples)} samples)")

    # Save subset
    subset_path = os.path.join(output_dir, "alpaca_formatted_subset.json")
    with open(subset_path, "w") as f:
        json.dump(subset, f, indent=2)
    print(f"Saved subset: {subset_path} ({len(subset)} samples)")

    # Preview a formatted example
    print("\n" + "=" * 60)
    print("FORMATTED EXAMPLE:")
    print("=" * 60)
    print(f"\n[INSTRUCTION]:\n{subset[0]['instruction']}")
    print(f"\n[RESPONSE]:\n{subset[0]['response']}")

    return all_samples, subset


if __name__ == "__main__":
    prepare_dataset()
